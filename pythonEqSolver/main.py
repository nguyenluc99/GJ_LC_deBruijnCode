def solver(twoDMatrix, size : int): # matrix: pure 2d numpy array
    nume_wc = 0
    sign = 1
    for idx in range(size-1):
        subMat = np.delete(twoDMatrix, idx, axis=1)
        spMat = sp.Matrix(subMat)
        det = spMat.det() * sign
        # print(idx, det)
        nume_wc = nume_wc + det
        sign *= -1
    deno_wc = sp.Matrix(np.delete(twoDMatrix, size-1, axis=1)).det() * sign
    # print(deno_wc)
    nume = deno_wc
    deno = ((1-2*x) * deno_wc - 2 * nume_wc).as_poly(x).as_expr()
    # print("============")
    # print(nume)
    # print("============")
    # print(deno)
    # print("============")
    return (nume, deno)

